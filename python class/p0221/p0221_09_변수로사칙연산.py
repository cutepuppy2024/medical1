# 두 수를 입력받아서 사칙연산을 출력해보세요
# 예: 30, 6을 입력받아서
# 출력 :
# 30 + 6 = 36
# 30 - 6 + 30
# 30 * 6 = 180
# 30 / 6 = 5.0


n1 = input("첫번째 숫자를 입력하세요 >> ")
n2 = input("두번째 숫자를 입력하세요 >> ")
